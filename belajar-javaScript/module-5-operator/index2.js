/* Comparison operator (operator perbandingan) */
// == untuk mengecek atau membandingkan nilai
let aaa = "5";
let bbb = 5;

// === untuk mengecek atau membandingkan nilai dan tipe data
let ccc = 5;
let ddd = 5;

// != untuk mengecek ketidaksamaan nilai dari variable
let eee = "5";
let fff = 3;

// !== untuk mengecek ketidaksamaan nilai dan tipe data dari variable
let ggg = "5";
let hhh = 3;

// > untuk mengecek apakah satu variable lebih besar dari variable lainnya
let iii = 11;
let jjj = 9;

// >= untuk mengecek apakah satu variable lebih besar atau sama dari variable lainnya
let kkk = 15;
let lll = 15;

// < untuk mengecek apakah satu variable lebih kecil dari variable lainnya
let mmm = 3;
let nnn = 9;

// <= untuk mengecek apakah satu variable lebih kecil atau sama dari variable lainnya
let ooo = 15;
let ppp = 15;

