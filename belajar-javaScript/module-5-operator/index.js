/* Operator aritmatika */
// Penjumlahan +
let a = 10;
let b = 5;
let c = a + b;

// Pengurangan -
let d = 20;
let e = 5;
let f = d - e;

// Perkalian *
let g = 4;
let h = 3;
let i = g * h;

// Pembagian /
let j = 30;
let k = 5;
let l = j / k;

// Modulus %
let m = 20;
let n = 3;
let o = m % n;

// Eksponen **
let p = 3;
let q = 3;
let r = p ** q;

// Increment
let s = 12;
s++;

// Decrement
let t = 10;
t--;

